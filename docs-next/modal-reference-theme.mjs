function makeTheme(name, type) {
  return new ExpressiveCodeTheme({
    ...modalThemeBase,
    name,
    type,
    colors: { ...modalThemeBase.colors },
    tokenColors: modalSyntaxRules.map((rule) => ({
      ...rule,
      scope: [...rule.scope],
      settings: { ...rule.settings },
    })),
  });
}

